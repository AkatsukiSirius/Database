---------- Linked Server
--- https://msdn.microsoft.com/en-us/library/ms188279.aspx

--- Configure a linked server to enable the SQL Server Database Engine 
--- to execute commands against OLE DB data sources outside of 
--- the instance of SQL Server. 
--- Typically linked servers are configured to enable the Database Engine 
--- to execute a Transact-SQL statement that includes tables 
--- in another instance of SQL Server, or another database product 
--- such as Oracle. 
--- Many types OLE DB data sources can be configured as linked servers, 
--- including Microsoft Access and Excel. Linked servers offer 
--- the following advantages:
--- 1) The ability to access data from outside of SQL Server.
--- 2) The ability to issue distributed queries, updates, commands, 
---    and transactions on heterogeneous data sources across the enterprise.
--- 3) The ability to address diverse data sources similarly.


